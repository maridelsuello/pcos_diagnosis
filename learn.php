    <h1>What is Ovarian Response Prediction?</h1>
    <p>
        Ovarian Response Prediction is a process that helps assess how a woman's ovaries might respond to fertility treatments, like IVF (in vitro fertilization). By analyzing factors such as age, BMI (body mass index), testosterone levels, follicle count, and menstrual cycle regularity, this system provides an insight into the likely success of fertility treatments.
    </p>

    <h2>Common Factors in Ovarian Response</h2>
    <ul>
        <li>Age – Fertility naturally declines with age, affecting ovarian response.</li>
        <li>BMI – Both low and high BMI can affect ovarian function and hormone balance.</li>
        <li>Testosterone Levels – Higher testosterone levels can influence egg production.</li>
        <li>Follicle Count – The number of follicles in the ovaries indicates the potential for egg retrieval during IVF.</li>
        <li>Menstrual Cycle Regularity – Irregular cycles may signal underlying hormonal issues affecting fertility.</li>
    </ul>

    <h2>About This Application</h2>
    <p>
        This web application allows users to input personal health information such as BMI, age, testosterone level,
        follicle count, and menstrual cycle regularity. The system uses these details to predict the likely ovarian response
        to fertility treatments, helping personalize treatment plans.
    </p>

    <p>
        The ovarian response prediction tool is not a substitute for professional medical advice. While it can guide personalized fertility planning, always consult a fertility expert or healthcare provider for confirmation and treatment options.
    </p>

    <h2>Features of the System</h2>
    <ul>
        <li><strong>Ovarian Response Prediction:</strong> The system predicts how a woman's ovaries are likely to respond to fertility treatments, such as IVF, based on input health data.</li>
        <li><strong>Health Data Tracking:</strong> Keep track of important health metrics such as BMI, menstrual cycle, and testosterone levels.</li>
        <li><strong>Response Categories:</strong>
            <ul>
                <li><strong>Poor Responder:</strong> Likely to produce fewer than 4 mature eggs during IVF stimulation. May indicate low ovarian reserve or low response to stimulation drugs.</li>
                <li><strong>Normal Responder:</strong> Likely to produce 4–15 mature eggs during IVF stimulation. Represents a healthy and expected response to treatment.</li>
                <li><strong>High Responder:</strong> Likely to produce more than 15 mature eggs during IVF stimulation. Suggests a very sensitive ovarian response, which may increase the risk of Ovarian Hyperstimulation Syndrome (OHSS) and require dosage adjustments.</li>
            </ul>
        </li>
    </ul>

